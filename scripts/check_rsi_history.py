def calculate_rsi_pure(prices, period=14):
    if len(prices) <= period:
        return [None] * len(prices)
        
    rsi_results = [None] * len(prices)
    
    # First Average Gain/Loss (Simple Mean)
    deltas = [prices[i] - prices[i-1] for i in range(1, len(prices))]
    
    avg_gain = sum([max(d, 0) for d in deltas[:period]]) / period
    avg_loss = sum([max(-d, 0) for d in deltas[:period]]) / period
    
    rsi_results[period] = 100 - (100 / (1 + (avg_gain / avg_loss))) if avg_loss != 0 else 100
    
    # Wilder's Smoothing
    for i in range(period + 1, len(prices)):
        delta = prices[i] - prices[i-1]
        gain = max(delta, 0)
        loss = max(-delta, 0)
        
        avg_gain = (avg_gain * (period - 1) + gain) / period
        avg_loss = (avg_loss * (period - 1) + loss) / period
        
        if avg_loss == 0:
            rsi_results[i] = 100
        else:
            rs = avg_gain / avg_loss
            rsi_results[i] = 100 - (100 / (1 + rs))
            
    return rsi_results

